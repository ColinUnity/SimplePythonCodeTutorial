from math import * #imports the math module

my_num = -5

print(my_num) # lets you use a variable(prints as normal)
print(str(my_num) + " evenest odd number") #lets you add a number to string
print(5) #prints the plain number(any number)
print(5+5) #prints the answer of the equation(can use order of operations)
print(10 % 3) #shows remainder
print(abs(my_num)) #gives you absoulute value
print(pow(3,2)) #raises to a power
print(max(5,10)) #tells the biggest number
print(min(5,10)) #shows the smallest number
print(round(4.5)) #rounds number 
print(floor(5.5)) #always rounds down
print(ceil(5.5)) #always rounds up
print(sqrt(25)) #prints square root of the number
