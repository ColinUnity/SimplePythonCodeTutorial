phrase = "colins s" #sets variable
print("\"Colins\"") #simple print command
print(phrase + " is pretty neat") #adds the variable to the another string
print(phrase.capitalize()) #capitilizes first letter
print(phrase.upper()) #changes the string to upper case letters
